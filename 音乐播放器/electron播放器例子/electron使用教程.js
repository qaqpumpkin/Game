/*
运行项目
1. npm install
2. npm run start

注意, 如果安装 electron 的时候网络不好, 可以通过切换源的方式来安装
具体操作如下
1, 输入下面的命令安装 cnpm(cnpm 详细解释参考 https://developer.aliyun.com/mirror/NPM)
npm install -g cnpm --registry=https://registry.npm.taobao.org

2, 输入 cnpm install 安装依赖

3, 输入 npm run start 运行程序



然后就会自动打开 electron 程序, 并且可以看到 electron 程序的 console
还可以看到命令行的运行状态

如果想重新运行程序, 比如说修改了代码之后重新运行程序
可以直接在 electron 程序里面刷新
点击菜单栏的「View」，然后选择「Reload」（快捷键 Mac 是 command + r, Windows 应该是 ctrl + r）

也可以使用另一种方式
在命令行里面输入 ctrl + c 中断程序运行
然后重新执行 npm run start
*/